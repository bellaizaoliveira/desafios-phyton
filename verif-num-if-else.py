# Verificador de Números com If e Else

'''
Será pedido ao usuário que digite um número. Se o número for 
maior que 10, imprima "O Número é maor que 10".Caso contrário, 
imprima "O número é menor ou igual a 10".

'''

numero = int(input('Digite um numero: '))

if numero > 10:
    print('O número é maior do que 10')

else: 
    print('O número é menor ou igual a 10')